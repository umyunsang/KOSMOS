# ---------------------------------------------------------------------------
# Invariant L1: genesis record has prev_hash == "0" * 64
# ---------------------------------------------------------------------------


def test_us2_genesis_prev_hash(ledger_env: dict) -> None:
    """First record appended must have prev_hash == '0' * 64 (Invariant L1)."""
    import json

    ledger_append(
        tool_id="test_tool",
        mode="default",
        granted=True,
        action_digest=hashlib.sha256(b"test").hexdigest(),
        ledger_path=ledger_env["ledger_path"],
        key_path=ledger_env["key_path"],
        key_registry_path=ledger_env["key_registry_path"],
    )

    lines = ledger_env["ledger_path"].read_text("utf-8").splitlines()
    rec = json.loads(lines[0])
    assert rec["prev_hash"] == "0" * 64, "Genesis prev_hash must be 64 zeros"


# ---------------------------------------------------------------------------
# Invariant L5 (WORM): append produces correct sequence numbers
# ---------------------------------------------------------------------------


def test_us2_sequence_increments(ledger_env: dict) -> None:
    """Sequence numbers must be monotonically increasing (Invariant L5 / L2)."""
    import json

    _write_5_records(ledger_env)
    lines = ledger_env["ledger_path"].read_text("utf-8").splitlines()
    sequences = [json.loads(line)["sequence"] for line in lines if line.strip()]
    assert sequences == list(range(5)), f"Unexpected sequences: {sequences}"


# ---------------------------------------------------------------------------
# Chain linkage: record[N].prev_hash == record[N-1].record_hash
# ---------------------------------------------------------------------------


def test_us2_chain_linkage(ledger_env: dict) -> None:
    """Every record's prev_hash must equal the previous record's record_hash."""
    import json

    _write_5_records(ledger_env)
    raw_lines = ledger_env["ledger_path"].read_text("utf-8").splitlines()
    lines = [json.loads(line) for line in raw_lines if line.strip()]

    assert lines[0]["prev_hash"] == "0" * 64
    for i in range(1, len(lines)):
        assert lines[i]["prev_hash"] == lines[i - 1]["record_hash"], (
            f"Chain broken at record {i}: "
            f"prev_hash={lines[i]['prev_hash']!r} "
            f"!= record_hash[{i - 1}]={lines[i - 1]['record_hash']!r}"
        )


# ---------------------------------------------------------------------------
# SC-004: Multiple single-byte flips at different positions all detected
# ---------------------------------------------------------------------------


@pytest.mark.parametrize("offset", [0, 10, 50, 100, 150, 200])
def test_us2_single_byte_flip_detected(ledger_env: dict, offset: int, tmp_path: Path) -> None:
    """SC-004: Single-byte flip at various offsets must be detected (exit != 0)."""
    # Write fresh records for each parametrized run.

    env = {
        "key_path": ledger_env["key_path"],
        "key_registry_path": ledger_env["key_registry_path"],
        "ledger_path": tmp_path / f"ledger_{offset}.jsonl",
        "tmp_path": tmp_path,
    }

    _write_5_records(env)
    ledger_path: Path = env["ledger_path"]
    raw = bytearray(ledger_path.read_bytes())

    if offset >= len(raw):
        pytest.skip(f"Offset {offset} beyond file size {len(raw)}")

    raw[offset] ^= 0xFF
    ledger_path.write_bytes(bytes(raw))

    report = verify_ledger(
        ledger_path=ledger_path,
        key_registry_path=env["key_registry_path"],
    )

    assert report.exit_code != 0, (
        f"Single-byte flip at offset {offset} was NOT detected! "
        f"exit_code={report.exit_code}, reason={report.broken_reason}"
    )
